import java.util.*;

public class HousingPrice {

	private String regionLevel;
	private double price;
	private Date date;
	private String name;
	
	public String getRegion() {
		return this.regionLevel;
	}
	
	public double getPrice() {
		return this.price;
	}
	
	public Date getDate() {
		return this.date;
	}
	
	public String getName() {
		return this.name;
	}
}
